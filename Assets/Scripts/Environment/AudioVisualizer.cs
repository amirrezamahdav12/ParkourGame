using UnityEngine;

public class AudioVisualizer : MonoBehaviour
{
    [Header("Audio Settings")]
    public AudioSource audioSource;
    public int sampleDataLength = 64; // ????? ????????? ?? ?? ??? ???????????? ??????

    [Header("Scale Settings")]
    public Vector3 baseScale = new Vector3(1, 1, 1); // ?????? ????? ????
    public float scaleMultiplier = 5f;               // ??? ????? ???? ??? ??????
    public float learnSpeed = 10f;                  // ???? ??? ??? ??????? (Smoothing)

    [Header("Axes Control")]
    public bool affectHorizontal = true;            // ????? ??? ???? ???? (X ? Z)
    public bool affectVertical = true;              // ????? ??? ???? ????? (Y)

    private float[] audioSamples;

    void Start()
    {
        audioSamples = new float[sampleDataLength];

        // ??? AudioSource ?????? ???? ???? ???? ??? ???? ??? ?????? ????
        if (audioSource == null)
        {
            audioSource = GetComponent<AudioSource>();
        }
    }

    void Update()
    {
        if (audioSource == null || !audioSource.isPlaying) return;

        // ?????? ???????? ???? ?? ????
        audioSource.GetSpectrumData(audioSamples, 0, FFTWindow.BlackmanHarris);

        // ?????? ??????? ????????? ???? ?? ??? ????? ???? ????
        float totalLoudness = 0f;
        for (int i = 0; i < sampleDataLength; i++)
        {
            totalLoudness += audioSamples[i];
        }
        float averageLoudness = totalLoudness / sampleDataLength;

        // ?????? ?????? ???? ?? ???? ???? ????
        float targetScaleX = baseScale.x + (affectHorizontal ? averageLoudness * scaleMultiplier : 0);
        float targetScaleY = baseScale.y + (affectVertical ? averageLoudness * scaleMultiplier : 0);
        float targetScaleZ = baseScale.z + (affectHorizontal ? averageLoudness * scaleMultiplier : 0);

        Vector3 targetScale = new Vector3(targetScaleX, targetScaleY, targetScaleZ);

        // ????? ??????? ?? ???? ??? (Lerp) ???? ??????? ?? ??? ???????
        transform.localScale = Vector3.Lerp(transform.localScale, targetScale, Time.deltaTime * learnSpeed);
    }
}