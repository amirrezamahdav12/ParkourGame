using System.Collections;
using UnityEngine;

public class PlayerAbilities : MonoBehaviour
{
    [Header("References")]
    public Transform orientation;
    private Rigidbody rb;
    private playerController pm;

    [Header("Double Jump")]
    public float doubleJumpForce = 10f;
    public KeyCode jumpKey = KeyCode.Space;
    private bool canDoubleJump;
    private bool isGrounded;

    [Header("Dash Settings")]
    public float dashForce = 25f;
    public float dashDuration = 0.25f;
    public float dashCooldown = 1f;
    public KeyCode dashKey = KeyCode.E;

    private bool isDashing;
    private bool canDash = true;

    private float horizontalInput;
    private float verticalInput;

    private void Start()
    {
        rb = GetComponent<Rigidbody>();
        pm = GetComponent<playerController>();
    }

    private void Update()
    {
        horizontalInput = Input.GetAxisRaw("Horizontal");
        verticalInput = Input.GetAxisRaw("Vertical");

        // ????? ????? ???? ?? ???? ??????? ????
        isGrounded = (pm.state != playerController.MovementState.air);

        if (isGrounded)
        {
            canDoubleJump = true; // ???? ??? ??? ???? ??? ????
        }

        // ?. ???? ??? ????
        if (Input.GetKeyDown(jumpKey) && !isGrounded && canDoubleJump && !pm.wallrunning)
        {
            DoubleJump();
        }

        // ?. ???? ?? (???? ? ???)
        if (Input.GetKeyDown(dashKey) && canDash && !isDashing)
        {
            StartCoroutine(Dash());
        }
    }

    private void DoubleJump()
    {
        canDoubleJump = false;

        // ??? ???? ???? Y ???? ??? ??? ???????
        rb.velocity = new Vector3(rb.velocity.x, 0f, rb.velocity.z);
        rb.AddForce(transform.up * doubleJumpForce, ForceMode.Impulse);
    }

    private IEnumerator Dash()
    {
        canDash = false;
        isDashing = true;

        // ????? ??????? ???? ? ??????? ???? ?? ?? ??? ??? ?? ???? ???? ??? ?? ???
        bool previousGravity = rb.useGravity;
        rb.useGravity = false;

        // ?????? ??? ?? ?? ???? ??????? ??? ????? (WASD)
        Vector3 dashDirection = orientation.forward * verticalInput + orientation.right * horizontalInput;

        // ??? ??? ????? ????? ???? ???? ?? ??? ??? ?? ???
        if (horizontalInput == 0 && verticalInput == 0)
            dashDirection = orientation.forward;

        // ??? ???? ???? ???? ???? ??????? ?? ????? ???? ???? ?? ???? ??
        rb.velocity = Vector3.zero;

        rb.AddForce(dashDirection.normalized * dashForce, ForceMode.Impulse);

        yield return new WaitForSeconds(dashDuration);

        // ?????????? ????? ?? ???? ????
        rb.useGravity = previousGravity;
        isDashing = false;

        // ?????? ???? ???????? ??
        yield return new WaitForSeconds(dashCooldown);
        canDash = true;
    }
}